package ge.edu.sangu.sangu2015.gogishvili.classes;

public class Rectangle {
	
	private double a;
	
	private double b;
	
	public double area() {
		return a * b;
	}

	public double getA() {
		return a;
	}

	public void setA(double a) {
		this.a = a;
	}

	public double getB() {
		return b;
	}

	public void setB(double b) {
		this.b = b;
	}

}